import React, {Component} from 'react';
import InputField from "../common/InputField";

export default class PersonFilter extends Component {

    constructor(props) {
        super(props);

        this.handleChange = this.handleChange.bind(this);
        this.handleSubmit = this.handleSubmit.bind(this);
    }

    handleChange(e) {
        this.props.handleChange(e);
    }

    handleSubmit(e) {
        this.props.handleSubmit(e);
    }

    render() {
        return (
            <form onSubmit={this.handleSubmit}>
                <InputField type="number" min="1" name={this.props.name} handleChange={this.handleChange}
                            label={this.props.label} prompt="neuveden" value={this.props.value}/>

                <input type="submit" className="btn btn-secondary float-right" value={this.props.confirm} />
            </form>
        );
    }

}